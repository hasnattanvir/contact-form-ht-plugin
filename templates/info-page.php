<div class="wrap">
    <h1>Contact Form HT Documentation</h1>
    <h2>Overview</h2>
    <p>This plugin allows you to add a contact form to your website, manage submissions, and perform actions such as edit, update, and delete entries.</p>

    <h2>Installation</h2>
    <ol>
        <li>Upload the plugin files to the `/wp-content/plugins/contact-form-plugin` directory, or install the plugin through the WordPress plugins screen directly.</li>
        <li>Activate the plugin through the 'Plugins' screen in WordPress.</li>
        <li>Use the shortcode <span style="color: blue;">`[contact_form]`</span> to add the contact form to any page or post.</li>
    </ol>

    <h2>Usage</h2>
    <h3>Adding the Contact Form</h3>
    <p>To add the contact form to a page or post, simply add the `[contact_form]` shortcode where you want the form to appear.</p>

    <h3>Admin Dashboard</h3>
    <p>The admin dashboard allows you to view, edit, update, and delete contact form submissions. Navigate to <strong>Contact Form > Contact Form Submissions</strong> in the WordPress admin menu to manage submissions.</p>

    <h3>Editing and Updating Entries</h3>
    <p>To edit or update an entry, click on the "Edit" link next to the entry in the submissions table. Make the necessary changes and click "Update Entry" to save the changes.</p>

    <h3>Deleting Entries</h3>
    <p>To delete an entry, click on the "Delete" link next to the entry in the submissions table. Confirm the deletion to remove the entry permanently.</p>

    <h2>Support</h2>
    <p>If you encounter any issues or have any questions, please contact support at <a href="mailto:linuxbangla@gmail.com">linuxbangla@gmail.com</a>.</p>
